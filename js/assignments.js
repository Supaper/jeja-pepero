function weekItems(rows, prefix) {
  const items = [];
  for (const [w, , due, lecture, life] of rows) {
    items.push({ id: `${prefix}w${w}-life`, kind: "생활간증", week: w, lecture, title: life, due: d(due[0], due[1]) });
  }
  return items;
}
function readingItems(rows, prefix) {
  return rows.map((r, i) => ({ id: `${prefix}r${i + 1}`, kind: "독서", title: r[0], due: d(r[1][0], r[1][1]) }));
}

